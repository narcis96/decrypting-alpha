Conduceam masina cu gindul aiurea , aproape fara sa vad peisajul . 
de ani si ani merg pe acelasi drum si am sentimentul ca il stiu pe de rost si ca drumul ma stie si el pe mine si ma duce unde trebuie . 
daca nu era vremea de moina care sa o zgribuleasca pe femeia de pe marginea soselei , probabil ca nu as fi vazut - o . 
am oprit si am luat - o . 
mergea pina la al doilea sat de la Rimnicu Vilcea . 
am luat doua banane de pe bancheta din spate . 
una pentru ea , alta pentru mine . 
nu stiu daca s - au scurs doua sau trei melodii cind am descoperit - o pe calatoare tot cu banana in mina . 
am intrebat - o , de ce nu mincati , doamna ? 
m - a privit lung si nu mi - a raspuns . 
pe urma , tot ea , putin jenata , a zis , va suparati daca nu o maninc si o duc acasa ? 
nu , doamna , am zis . 
de ce ? 
am un copil in clasa intii si de trei ani nu i - am mai putut cumpara o banana ! 
la primul chiosc de pe marginea soselei am oprit , am cumparat doua kilograme de banane pentru pusti si i - am spus la revedere . 
asta e tot ce am putut sa fac in fata unui gest prin care am fost obligat sa redescopar lumea in care traim . 
cit valoreaza o libertate in care nu poti sa - ti platesti incalzirea apartamentului ? 
cita valoare turistica , umana , culturala si istorica are un oras debransat de la caldura ? 
cit valoreaza drepturile omului fara banii necesari pentru a cumpara un bilet de tren sau de autobuz ? 
cit poate sa inteleaga cineva din spusele unui politician sau din ce scrie un ziarist daca de trei ani nu are bani sa cumpere o banana pentru copil ? 
ce importanta au filmele despre locuri exotice sau despre alte civilizatii cind un om nu - si poate cumpara algocalmin ? 
cit valoreaza o democratie incipienta si ce sanse are ea daca omul nevoias e obligat sa se uite din ce in ce mai des spre tomberonul pe linga care trece ? 
cit de sanatos este un sistem in care unul fura de la stat milioane de dolari , iar cei multi string din masele cind trec pe linga vitrine ? 
in ce masura mai merita si poti sa trudesti pentru un ideal maret cind ti - e teama sa te rezemi de orice ? 
de un prieten , de o institutie publica , de un politician ? 
cit timp mai poti crede in steaua ta ? 
cum sa mai speri ca intr - o buna zi o vei scoate la liman cu toate necazurile cind preturile se indexeaza in fiecare luna cu rata inflatiei , iar lefurile cu mult mai putin ? 
e ca si cum ai fugi descult dupa soarele care apune . 
ce vreau sa spun , pornind de la o banana tinuta de o femeie ca pe un trofeu pretios pentru copilul ei ? 
ca exista riscul sa ne imbatam cu siguranta nationala , cu integrarea euroatlantica si cu multe alte cauze majore , uitind nevoile simple . 
oamenilor le este foame si le este frig . 
si dincolo de aceste adevaruri crunte nu mai conteaza decit moartea . 
sa traiesti si sa visezi sint aventuri din care scapa extrem de putini . 
daca nu ne putem intoarce la aceste adevaruri dureroase si daca  viata societatii nu e guvernata de dorinta de a solutiona aceste lipsuri elementare , toate celelalte nu sint decit mofturi , floricele , scinceli si maimutareala . 
daca e din ce in ce mai greu sa fii om , tot spectacolul politic nu mai face doi bani . 
este chiar forma nerecunoscuta de esec . 
