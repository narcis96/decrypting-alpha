trezirea din pumn ( 2 ) 
Miercurea trecuta , Emil Constantinescu a incercat sa se trezeasca din pumnii incasati in acesti ani . 
" mi - a luminat mintea " . 
" m - am hotarit sa lupt " . 
" Romania este sub imperiul minciunii liber consimtite " . 
" am obtinut dreptul la expresie , dar am ales dreptul de a minti " . 
afirmatiile sint generale si transante . 
la o prima vedere , ele sint de - a dreptul scandaloase . 
in acelasi spirit , li se poate raspunde cu proteste la fel de generale . 
dar cistigul ar fi infim . 
presedintele zice , iar ceilalti contesta . 
cu ce ne - am ales ? 
raminem in aceeasi situatie fara iesire ca si pina acum . 
nu putem sa nu recunoastem ca , din 1990 , Romania a fost bombardata cu tot felul de afirmatii socante . 
va mai aduceti aminte de drogurile din Piata Universitatii ? 
dar de aventurile lui Nicu Ceausescu puse pe seama unei disidente cunoscute ? 
dar de fabricile de dolari ale opozitiei ( in iunie 1990 minerii au cautat si la " Expres " o fabrica de dolari ) ? 
nu au fost alegerile din 20 mai 1990 si cele din 1992 opera unei masive manipulari prin mijloacele publice , dar si prin cele private , folosite cu abilitate in acest scop ? 
nu a aterizat regele linga Oradea prin 1990 pentru a rasturna tinara putere democratica ( nu rideti ! ) ? 
nu a existat atunci libertate de exprimare ? 
au fost ziare si radiouri care au spus adevarul ! 
dar nu au raspindit celelalte aceste minciuni liber consimtite ? 
povestea cu Emil Constantinescu spion - care a livrat informatii despre nu stiu ce zacaminte in perioada in care era lector universitar - nu este tot o minciuna pe care n - a dovedit - o nimeni , dar a fost consimtita de media ? 
a dezmintit cineva ca seful statului este regele cafelei si al doilea om bogat din Romania ? 
un sondaj de opinie chiar l - a creditat pe Emil Constantinescu intre primii trei magnati ai tarii . 
poate cineva sa dovedeasca un asemenea lucru ? 
are indicii pentru o astfel de afirmatie ? 
si totusi sondajul respectiv constata ca o asemenea aberatie circula in opinia publica ! 
este ea rodul intimplarii ? 
chiar asa din senin , la Jibou , la Bolintin sau la Babadag s - a conturat o asemenea opinie ? 
cum a fost ea construita ? 
in ce scop ? 
nu este aceasta o minciuna liber consimtita care e luata drept adevar batut in cuie ? 
este aceasta perceptie impanata cu aberatii doar rodul neputintei lui Emil Constantinescu de a stringe in jurul sau o echipa capabila sa ofere o imagine mai apropiata de realitate ? 
sau de partea cealalta actioneaza o alta echipa mult mai abila si mai performanta decit cea a presedintelui ? 
vrind - nevrind , trebuie sa ne intoarcem la Adrian Severin . 
parca n - a fost chiar asa de nebun cind a vorbit de spioni , de agenti in partide si in mass - media ! 
raportul SRI , depus si el de aproape un an la parlament , vorbeste de preocupari pentru cultivarea unor agenti de influenta . 
Virgil Magureanu afirma ca structurile fostei Securitati au o foarte buna reprezentare in parlament si crede ca " a venit iarasi vremea lor " . 
Emil Hurezeanu declara intr - un interviu ca fortele care n - au dorit o evolutie democratica in Romania se pregatesc sa cistige alegerile . 
Emil Constantinescu ataca direct . 
in discutia de la TVR , el a spus ca " aparatul de dezinformare s - a mutat la PDSR " . 
daca revenim la acuzatiile cu spionajul , cu cel mai bogat om din Romania , cu regele cafelei , sintem obligati sa acceptam ca ele sint totusi rezultatul unei campanii abile de dezinformare . 
Emil Constantinescu a facut un car de greseli . 
pacatuieste prin orgoliu , prin incrincenare , prin egolatrie si prin multe altele . 
dar pina la acuzatii menite a - l matura de pe scena mai este totusi mult . 
iar opera de pulverizare a credibilitatii sale nu vine nici din Zambia , nici din Republica Dominicana sau din Albania ! 
se desfasoara in Romania si are la origine forte politice si grupuri de interese hotarite sa - l scoata definitiv din jocul politic . 
in 1992 , Emil Constantinescu a pierdut lamentabil . 
nimeni nu - i mai dadea vreo sansa . 
si totusi " profesorasul " ( cum il ironizau unii ) a supravietuit , a recuperat si chiar a cistigat . 
acum pare a fi din nou la pamint . 
Atacind violent opozitia si mecanismele dezinformarii , Emil Constantinescu da semne ca incearca sa se trezeasca din pumnii incasati . 
