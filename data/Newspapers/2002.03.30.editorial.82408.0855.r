de partea cealalta , desfacerea de carte este catastrofala . 
romanii disperati sa faca afaceri au tabarit pe spatiile librariilor si le - au cucerit cu forta , ajungind ca , in loc sa vinda cultura , stiinta si informatii , sa comercializeze chiloti , tirlici , oale si alte comedii . 
iar primarii oraselor romanesti , in loc sa apere aceste mici cetati de cultura , s - au napustit pe ele cu chirii si taxe , de parca vinzarea cartii ar fi la fel de profitabila ca si comertul cu arme si droguri . 
toata incercarea lor de a aduce investitori in respectivele orase , oferindu - le spatiile librariilor , nu face decit sa duca la o ruinare a natiei . 
cititul incepe sa fie o indeletnicire desueta , iar achizitionarea unei carti - o operatiune stupida in care alegi ceva de la talciocul de pe bulevard . 
in Romania , cartea a fost gonita din spatii solemne si alungata pe tarabele de pe strazi , sub plastice murdare , in praf si printre degete slinoase . 
ca in Africa , in Romania cartea se vinde in piete , pe unde apuca , pe la colturi sau pe sub stresini , de parca ar fi egala prezervativelor sau a sireturilor . 
Pampalaii de primari din scumpa noastra patrie isi imagineaza ca alegerea unei carti este similara cu bautul unui sprit si mincatul unui covrig . 
nu necesita nici meditatie , nici intilnire solemna , nici gust si nici liniste . 
prin disparitia treptata a librariilor , in Romania se produce barbarizarea populatiei . 
cartea se vinde in conditii mai mizere decit o legatura de ridichi , iar cititul incepe sa fie o indeletnicire pentru visatori . 
