Cornel Nistorescu de Cornel Nistorescu Luni , 17 Decembrie 2001 Nu stiu ce l - a determinat pe Thomas Gottschalk sa lanseze provocarea de la ZDF . 
putea foarte bine sa se gindeasca la altceva sau la alta tara . 
si , la fel de bine , putea sa incerce o emisiune prin care sa aduca bucurie in sufletele copiilor din Germania . 
oricit de multe jucarii ar avea cei de acolo , tot mai este loc pentru o raza de fericire in sufletul unui nevinovat . 
or , Thomas Gottschalk a incercat sa ii adune pe micutii din Germania in jurul unei idei generoase . 
haideti sa facem o bucurie celor din Romania ! 
nu a rostit cuvintul ajutoare , nu a invocat mila sau compasiune ! 
pur si simplu a dorit sa - i convinga pe parintii si pe copiii din Berlin sa faca un bine . 
indaratul initiativei lansate de vedeta germana e un crez crestin . 
numai  in masura in care sintem in stare sa oferim altora ceea ce au nevoie ne inaltam in fata noastra , a semenilor , a lui Dumnezeu . 
ieri , in mai putin de 24 de ore , in Potsdamer Platz s - au strins 5 . 000 de pachete cu cadouri . 
nemtii au raspuns prompt unei provocari generoase . 
in acelasi timp , Traian Basescu a reactionat frumos . 
daca ei s - au gindit la asta , haideti sa le facem si noi o surpriza ! 
sa le trimitem corul Alegretto si sa le oferim tuturor celor care vin cu daruri cite un compact disc cu muzica frumoasa de la noi . 
daca intreg convoiul va birui pe drumurile cu nameti si va ajunge miine in fata Salii Palatului , mii de copii din Romania se vor bucura inca o data . 
iar povestea va calatori prin sufletele a milioane de telespectatori din Romania si din Germania . 
ea contine un mesaj asupra caruia merita sa zabovim . 
nu doar Thomas Gottschalk , Traian Basescu , TVR , ZDF sau guvernul au obligatia morala de a incerca sa faca bine . 
este datoria fiecarui om de pe lumea asta sa faca acest lucru . 
unde poate , cind poate , cu ce poate sa ofere si el o bucurie . 
eu cred ca  Dumnezeu , de cind il trimite pe om in lumina , ii spune , fa si tu un bine ! 
